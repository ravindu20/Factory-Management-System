    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //String name = jTextField1.getText();
        String toMail = jTextField3.getText();
        String subject = jTextField2.getText();
        String msg = jTextArea1.getText();
        
        String host="smtp.gmail.com";  
        final String user="temp.fms@gmail.com";//change accordingly  
        final String password="TempFms1234";//change accordingly  

        String to=toMail;//change accordingly  

         //Get the session object  
        Properties props = new Properties();  
        props.put("mail.smtp.host",host);  
        props.put("mail.smtp.auth", "true");  
        props.put("mail.smtp.port", "587");
        props.put("mail.smtp.ssl.trust", "smtp.gmail.com");
        props.put("mail.smtp.starttls.enable", "true");



         Session session = Session.getDefaultInstance(props,  
         new javax.mail.Authenticator() {  
           protected PasswordAuthentication getPasswordAuthentication() {  
         return new PasswordAuthentication(user,password);  
           }  
     });  

     //Compose the message  
      try {  
       MimeMessage message = new MimeMessage(session);  
       message.setFrom(new InternetAddress(user));  
       message.addRecipient(Message.RecipientType.TO,new InternetAddress(to));  
       message.setSubject(subject);  
       message.setText(msg);  

      //send the message  
       Transport.send(message);  

       JOptionPane.showMessageDialog (null, "Email was sent successfully !", "Success", JOptionPane.INFORMATION_MESSAGE);

       } catch (MessagingException e) {e.printStackTrace();
          System.out.println(e);
       } 
    }//GEN-LAST:event_jButton2ActionPerformed

