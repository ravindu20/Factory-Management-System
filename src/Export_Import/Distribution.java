    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        // Remove Driver And Assistant
        try{
            if(!DriverID.getText().isEmpty() && !AssistaantID.getText().isEmpty()){
                int x = JOptionPane.showConfirmDialog(rootPane, "Do you want to Allocate these Driver and Assistant ? ");
                if(x == 0){
                    Connection c = db.mycon();
                    Statement s = c.createStatement();
                    s.executeUpdate("Delete from driver_vehicle where DriverName = '"+DriverID.getText()+"' and AssistantName = '"+AssistaantID.getText()+"'");
                    s.executeUpdate("UPDATE driver SET availability = '" +"Yes"+ "' where Name = '" + DriverID.getText() + "' ");
                    s.executeUpdate("UPDATE driver SET availability = '" +"Yes"+ "' where Name = '" + AssistaantID.getText() + "' ");
                    JOptionPane.showMessageDialog(rootPane, "Allocated Drivers and Assistants remove Sucessfully");
                    DriverID.setText("");
                    AssistaantID.setText("");
                    ResultSet rs = s.executeQuery("Select * from driver_vehicle");
                    driver_vehicletable.setModel(DbUtils.resultSetToTableModel(rs));
                }
                else if(x== 1){
                    DriverID.setText("");
                    AssistaantID.setText("");
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Fill Empty Field");
            }
            
        }
        catch(Exception e){
           // e.printStackTrace();
        }
    }//GEN-LAST:event_removeActionPerformed

