    private void driversActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_driversActionPerformed
        // Driver Panel:
        Main.removeAll();
        Main.add(Drivers);
        Main.repaint();
        Main.revalidate();
        
        //get available drivers and assistants names into the jlist
        try{
            DefaultListModel driver = new DefaultListModel();
            DefaultListModel assistant = new DefaultListModel();
            Connection c = db.mycon();
            Statement s = c.createStatement();
            //Statement s2 = c.createStatement();
            
            //fill the driver_vehicle table
            ResultSet rs3 = s.executeQuery("Select * from driver_vehicle");
            driver_vehicletable.setModel(DbUtils.resultSetToTableModel(rs3));
            
            ResultSet rs1 = s.executeQuery("select Name from driver where type = '"+"Driver"+"' and availability = '"+"Yes"+"'");
            while(rs1.next()){
                String dname = rs1.getString("Name"); //get the element in column "Name"
                driver.addElement(dname); //add each item to the driver
            }
            jList1.setModel(driver);
            
            ResultSet rs2 = s.executeQuery("select Name from driver where type = '"+"Assistant"+"' and availability = '"+"Yes"+"'");
            while(rs2.next()){
                String aname = rs2.getString("Name"); //get the element in column "Name"
                assistant.addElement(aname); //add each item to the driver
            }
            jList2.setModel(assistant);
    
        }
        catch(Exception e){
            //e.printStackTrace();
        }

        
    }//GEN-LAST:event_driversActionPerformed

