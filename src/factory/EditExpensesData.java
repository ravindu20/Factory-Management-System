    public void update(String id){
        db databaseCon = new db();
        Connection dbconnect = db.dbconnect();
        
        String cusId =id;
        int intId=Integer.parseInt(cusId);
        String Cname = name.getText();
        String CcomName = company_name.getText();
        String CconNum = contact_number.getText();
        String Cemail = email_address.getText();
        String Caddr = address.getText();
        String Cdat = date.getText();
        String Corder = (String) order_type.getSelectedItem();
        double Cquan = Double.parseDouble(quantity.getText());
        String Ccust = (String) customer_type.getSelectedItem();


        if(name.equals("")||company_name.equals("")||contact_number.equals("")||email_address.equals("")||address.equals("")||date.equals("")||order_type.equals("")||quantity.equals("")||customer_type.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please fill out the empty fields !");
        } else {
            try{
                Connection con = databaseCon.dbconnect();
                ResultSet rs = null;
                PreparedStatement pst = null;
                pst = con.prepareStatement("UPDATE customer SET Name = ?, Company_name = ?, Contact_number = ?, Email_address = ?,Address = ? , Date = ?, order_type =?, Quantity = ?, Customer_type = ?  WHERE Customer_id = ?");
                pst.setString(1,Cname);
                pst.setString(2,CcomName);
                pst.setString(3,CconNum);
                pst.setString(4,Cemail);
                pst.setString(5,Caddr);
                pst.setString(6,Cdat);
                pst.setString(7,Corder);
                pst.setDouble(8,Cquan);
                pst.setString(9,Ccust);
                pst.setInt(10,intId);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Changes Saved !");
                
            }catch (Exception e){
                System.out.println(e);
            }
        }
        customer obj = new customer();
                obj.ViewCustomerDetails();
    }
    
