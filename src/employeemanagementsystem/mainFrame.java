    //Payroll: Calculate deductions
    public void calcDeductions() {
        
        if(validateDeductionFields()){
        try {
            
            s = c.createStatement();
            String salary_value = (String)combobox_empnosalary.getSelectedItem();
            rs = s.executeQuery("SELECT salary.NoPayLeaves "
                                         + "FROM salary WHERE salary.empNo ='" + salary_value + "'");

            while (rs.next()) {
                txt_NopayDays.setText(rs.getString("NoPayLeaves"));
            }
            
            String catSalary = txt_categorySalary.getText();
            double basicSalary,NopayDays,NopayAmount,totDeduct;
            double totEPF,empEPF,salaryAdvance,festivalAdvance,stampDuty;
            double OTAmount,nightIncentive,grossSalary,netSalary;
            double NopayRate = 0;
            basicSalary = Double.valueOf(txt_basicSalary.getText());
            NopayDays = Double.valueOf(txt_NopayDays.getText());
            
            switch (catSalary) {
                case "Shop and office":
                    NopayRate = Math.round(basicSalary/30.0);
                    break;
                case "Wages board":
                    NopayRate = Math.round(basicSalary/26.0);
                    break;
            }
                     
            txt_NopayRate.setText("" + NopayRate);
            NopayAmount = NopayRate*NopayDays;
            txt_NopayAmount.setText("" + NopayAmount);
            
            OTAmount = Double.valueOf(txt_OTAmount.getText());
            nightIncentive = Double.valueOf(txt_NightAllowance.getText());
            totEPF = Math.round(basicSalary+OTAmount+nightIncentive-NopayAmount);
            txt_totEPF.setText("" + totEPF);
            
            empEPF = Math.round((totEPF/100.0)*8.0);
            txt_empEPF.setText("" + empEPF);
            
            salaryAdvance = Double.valueOf(txt_salaryAdvance.getText());
            festivalAdvance = Double.valueOf(txt_festivalAdvance.getText());
            grossSalary = Double.valueOf(txt_grossSalary.getText());
            
            if(grossSalary > 25000.00){
            
                stampDuty = 25.00;
            
            }else{
            
                stampDuty = 0.00;
            
            }
            
            txt_stampDuty.setText("" + stampDuty);
            
            totDeduct = NopayAmount+empEPF+salaryAdvance+festivalAdvance+stampDuty;
            txt_totDeductions.setText("" + totDeduct);
            
            netSalary = grossSalary-totDeduct;
            txt_netSalary.setText("" + netSalary);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        }
    }
    
