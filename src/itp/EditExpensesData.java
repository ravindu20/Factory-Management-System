    public void update(String id){
        db databaseCon = new db();
        Connection dbconnect = db.dbconnect();
        
        String exID = id;
        int intID = Integer.parseInt(exID);
        String exName = editExName.getText();
        String exType = editExType.getText();
        double exAm = Double.parseDouble(editExAmt.getText());
        String exDate = ((JTextField)editExDate.getDateEditor().getUiComponent()).getText();

        if(exName.equals("")||exType.equals("")||exDate.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please fill out the empty fields !");
        } else {
            try{
                Connection con = databaseCon.dbconnect();
                ResultSet rs = null;
                PreparedStatement pst = null;
                pst = con.prepareStatement("UPDATE expenses SET name = ?, type = ?, amount = ?, date_payed = ? WHERE id = ?");
                pst.setString(1,exName);
                pst.setString(2,exType);
                pst.setDouble(3,exAm);
                pst.setString(4,exDate);
                pst.setInt(5,intID);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(rootPane, "Changes Saved !");
               
            }catch (Exception e){
                System.out.println(e);
            }
        }
    }
    
