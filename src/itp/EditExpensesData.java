    private void editExNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_editExNameKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isLetter(c) || (c==KeyEvent.VK_BACK_SPACE) || (c==KeyEvent.VK_SPACE))){
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_editExNameKeyTyped

